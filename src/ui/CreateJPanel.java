    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        
        String name = txtname.getText();
        int employeeID = Integer.parseInt(txtemployeeid.getText());
        int age = Integer.parseInt(txtage.getText());
        String gender = txtgender.getText();
        String startdate = txtstartdate.getText();
        String level = txtlevel.getText();
        String teaminfo = txtteaminfo.getText();
        String positiontitle = txtpositiontitle.getText();
        String cellphone = txtcellphonenumber.getText();
        String email = txtemail.getText();
        
        employee emp = history.addNewEmployee();
        
        emp.setName(name);
        emp.setEmployeeID(employeeID);
        emp.setAge(age);
        emp.setGender(gender);
        emp.setStartDate(startdate);
        emp.setLevel(level);
        emp.setTeaminfo(teaminfo);
        emp.setPositiontitle(positiontitle);
        emp.setCellPhone(cellphone);
        emp.setEmail(email);
        
        JOptionPane.showMessageDialog(this, "New Employee Added.");
        
        txtname.setText("");
        txtemployeeid.setText("");
        txtage.setText("");
        txtgender.setText("");
        txtstartdate.setText("");
        txtlevel.setText("");
        txtteaminfo.setText("");
        txtpositiontitle.setText("");
        txtcellphonenumber.setText("");
        txtemail.setText("");
   
    }//GEN-LAST:event_btnsaveActionPerformed

    private void txtnameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtnameKeyReleased
        // TODO add your handling code here:
        String PATTERN ="^[a-zA-Z0-9]{0,30}$";
        Pattern name = Pattern.compile(PATTERN);
        Matcher match = name.matcher(txtname.getText());
         if(!match.matches()){
             lblnamevalidation.setText("Please Enter correct data!");
         }
         else{
             lblnamevalidation.setText(null);
         }
        
        
    }//GEN-LAST:event_txtnameKeyReleased

